    void xmlResult() {
        try {
            String url = "https://www.tcmb.gov.tr/kurlar/today.xml";
            String data = Jsoup.connect(url).get().toString();
            
            Document doc = Jsoup.parse(data, "", Parser.xmlParser());
            Elements elm = doc.getElementsByTag("Currency");
            int i = 0;
            for (Element item : elm) {
                String Isim = item.getElementsByTag("Isim").text();
                String ForexBuying = item.getElementsByTag("ForexBuying").text();
                String ForexSelling = item.getElementsByTag("ForexSelling").text();
                System.out.println("isim : " + Isim + " ForexBuying :" +ForexBuying + " ForexSelling :" + ForexSelling);
                if(i == 0) {
                    da.setText(ForexBuying);
                    ds.setText(ForexSelling);
                }
                
                if (i == 3) {
                    ea.setText(ForexBuying);
                    es.setText(ForexSelling);
                }
                i++;
            }
        } catch (Exception e) {
        }
    }
    
    
